    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        //list
         String uninades;
        int total=0,producto=0,destino=0,envio=0,uni=0;
        if(ltsProductos.isSelectedIndex(0)){
            producto=120;
        }
        if(ltsProductos.isSelectedIndex(1)){
            producto=60;
        }
        if(ltsProductos.isSelectedIndex(2)){
            producto=45;
        }
        //combobox destino
        if(cmbDestino.getSelectedIndex()== 0){
            destino=20;
        }
        if(cmbDestino.getSelectedIndex()== 1){
            destino=25;
        }
        if(cmbDestino.getSelectedIndex()== 2){
            destino=30;
        }
        //combobox envio
        if(cmbEnvio.getSelectedIndex()== 0){
            envio=3;
        }
        if(cmbEnvio.getSelectedIndex()== 1){
            envio=10;
        }
        uninades=txtUnidades.getText();
        uni=Integer.parseInt(uninades);
        total=(uni*producto)+destino+envio;
        lblInformacion.setText(Integer.toString(total));
    }//GEN-LAST:event_btnCalcularActionPerformed

